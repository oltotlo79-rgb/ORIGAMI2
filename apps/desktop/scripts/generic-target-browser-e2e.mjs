import { chromium } from 'playwright'
import { spawn } from 'node:child_process'
const origin = 'http://127.0.0.1:4190'
const server = spawn(process.execPath, ['./node_modules/vite/bin/vite.js', '--host', '127.0.0.1', '--port', '4190', '--strictPort'], { cwd: process.cwd(), stdio: 'ignore' })
let browser
try {
  for (let i = 0; i < 150; i += 1) { try { if ((await fetch(origin)).ok) break } catch {}; await new Promise((r) => setTimeout(r, 100)) }
  browser = await chromium.launch({ headless: true }); const page = await browser.newPage()
  await page.goto(`${origin}/scripts/generic-target-browser-harness.html`, { waitUntil: 'networkidle' })
  await page.getByRole('button', { name: 'Create empty generic target' }).click(); await assertBindings(page)
  const add = page.getByRole('button', { name: 'Add binding' })
  for (let count = 2; count < 8; count += 1) await add.click()
  if (await page.getByRole('list', { name: 'Editable generic target dimensions' }).getByRole('listitem').count() !== 8) throw new Error('eight-binding upper boundary failed')
  if (await add.isEnabled()) throw new Error('ninth binding was not blocked')
  await page.getByRole('button', { name: 'Try oversized target' }).click()
  if (await page.getByRole('list').count()) throw new Error('oversized target reached UI')
  page.once('dialog', (dialog) => dialog.accept())
  await page.getByRole('button', { name: 'Review and copy contours to editor' }).click(); await assertBindings(page)
  if ((await page.getByLabel('Local outline points binding 1').inputValue()).split('\n').length !== 5) throw new Error('recognized local contour was not copied')
  await page.getByText('Contour approximation score: 82', { exact: true }).waitFor()
  const editor = page.getByRole('list', { name: 'Editable generic target dimensions' })
  await page.getByLabel('Body outline points').fill('-5,-5\n5,-5\n5,5\n-5,5')
  await page.getByRole('button', { name: 'Apply outline' }).click()
  const symmetricOutline = (await page.getByLabel('Body outline points').inputValue()).replaceAll(' ', '')
  if (symmetricOutline.split('\n').length !== 4 || !symmetricOutline.includes('-5,5')) throw new Error('symmetric body outline was not accepted')
  await page.getByLabel('Body outline points').fill('0,0\n1,0\n1,1')
  await page.getByRole('button', { name: 'Apply outline' }).click()
  await page.getByRole('alert').waitFor()
  await page.getByLabel('Body outline mode').selectOption('general')
  await page.getByLabel('Body outline points').fill('-5,-5\n5,-5\n4,5\n-3,5')
  await page.getByRole('button', { name: 'Apply outline' }).click()
  const generalOutline = (await page.getByLabel('Body outline points').inputValue()).replaceAll(' ', '')
  if (generalOutline.split('\n').length !== 4 || !generalOutline.includes('-3,5')) throw new Error('general asymmetric outline was not accepted')
  const bodyCanvas = page.getByRole('img', { name: 'Body outline preview' })
  await bodyCanvas.focus(); await page.keyboard.press('ArrowRight')
  await page.waitForFunction((before) => document.querySelector('textarea[aria-label="Body outline points"]')?.value.replaceAll(' ', '') !== before, generalOutline)
  await page.getByLabel('Thickness binding 1 (mm)').fill('0')
  if (await page.getByLabel('Thickness binding 1 (mm)').inputValue() !== '5') throw new Error('invalid dimension was accepted')
  await page.getByLabel('Local outline points binding 1').fill('-2,-1\n2,-1\n0,2')
  await editor.getByRole('button', { name: 'Apply local outline' }).first().click()
  if ((await page.getByLabel('Local outline points binding 1').inputValue()).split('\n').length !== 3) throw new Error('local outline was not accepted')
  await page.getByLabel('Outline to preview').selectOption('1')
  await page.getByRole('img', { name: 'Binding 1 outline preview' }).waitFor()
  const painted = await page.getByRole('img', { name: 'Binding 1 outline preview' }).evaluate((canvas) => {
    const context = canvas.getContext('2d'); return context.getImageData(0, 0, canvas.width, canvas.height).data.some((value) => value !== 0)
  })
  if (!painted) throw new Error('selected local outline was not painted')
  await editor.getByRole('button', { name: 'Move down' }).first().click()
  if (await page.getByLabel('Length binding 1 (mm)').inputValue() !== '22') throw new Error('reorder was not canonicalized')
  if (await page.getByLabel('Part kind binding 1').inputValue() !== 'fin') throw new Error('part kind was detached during reorder')
  await page.getByLabel('Mount vertical binding 1 (mm)').fill('12.5')
  await page.getByLabel('Mount fore-aft binding 1 (mm)').fill('-3.5')
  await page.getByLabel('Direction horizontal binding 1').fill('0.75')
  await page.getByLabel('Bilateral spacing binding 1 (mm)').fill('6.5')
  if (await page.getByLabel('Mount vertical binding 1 (mm)').inputValue() !== '12.5'
    || await page.getByLabel('Mount fore-aft binding 1 (mm)').inputValue() !== '-3.5'
    || await page.getByLabel('Direction horizontal binding 1').inputValue() !== '0.75') throw new Error('mount geometry edit was not retained')
  await page.getByLabel('Part kind binding 1').selectOption('wing')
  if (await page.getByLabel('Part kind binding 1').inputValue() !== 'wing') throw new Error('part kind was not editable')
  await page.getByRole('button', { name: 'Add binding' }).click()
  if (await editor.getByRole('listitem').count() !== 3) throw new Error('binding was not added')
  await editor.getByRole('button', { name: 'Remove' }).last().click()
  if (await editor.getByRole('listitem').count() !== 2) throw new Error('binding was not removed')
  if (await editor.getByRole('button', { name: 'Remove' }).first().isEnabled()) throw new Error('two-binding lower bound was not enforced')
  await page.getByLabel('Symmetry binding 1').selectOption('none')
  await page.getByRole('list', { name: 'Bounded generic target binding dimensions' }).getByText(/^Binding 1 · asymmetric single · count 1/).waitFor()
  await page.getByLabel('Length binding 1 (mm)').fill('31.5')
  await page.getByRole('list', { name: 'Bounded generic target binding dimensions' }).getByText(/length 315/).waitFor()
  await page.getByRole('button', { name: 'Evaluate generic target grid' }).click()
  const preview = page.getByRole('region', { name: 'Generic target candidate preview' }); await preview.waitFor()
  await page.getByRole('button', { name: 'Replace recognized target' }).click(); await preview.waitFor({ state: 'detached' })
  await page.getByRole('button', { name: 'Recognize mixed target GLB' }).click(); await assertBindings(page)
  await page.getByRole('button', { name: 'Evaluate generic target grid' }).click()
  await page.getByRole('button', { name: 'Cancel generic target grid' }).click(); await preview.waitFor({ state: 'detached' })
  await page.getByRole('button', { name: 'Evaluate generic target grid' }).click()
  await page.getByRole('button', { name: 'Confirm and apply generic target' }).click(); await preview.waitFor({ state: 'detached' })
  await page.waitForFunction(() => document.activeElement?.textContent === 'Evaluate generic target grid')
  for (const [button, status] of [['Undo generic target', 'Generic target undone'], ['Redo generic target', 'Generic target redone'], ['Save and reopen generic target', 'Generic target saved and reopened']]) {
    await page.getByRole('button', { name: button }).click(); await page.getByText(status, { exact: true }).waitFor()
  }
  console.log('generic target browser E2E passed: image/GLB, add/remove/reorder bounds, stale/cancel, apply history/save')
} finally { await browser?.close(); server.kill('SIGTERM') }
async function assertBindings(page) {
  const list = page.getByRole('list', { name: 'Bounded generic target binding dimensions' }); await list.waitFor()
  if (await list.getByRole('listitem').count() !== 2) throw new Error('generic binding count changed')
}
